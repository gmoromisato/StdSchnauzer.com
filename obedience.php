			<img style="padding-right: 4px;" src="images/obedience/obed1.jpg" align="left" width="325" height="197" alt="obedience jump">
			To enter an Obedience competition, a dog must be six months of age, be AKC 
			registered and not be lame. Dogs with ILP registration numbers and dogs that 
			have been spayed or neutered may compete in obedience trials. The purpose of the 
			Obedience training is to teach the dog that he MUST obey and the purpose of the 
			trial is to demonstrate that he WILL obey.
			<br /><br />
			Obedience Trials are divided into classes or levels for competition. Beginners enter 
			a class called Novice A or Novice B. Novice A is for anyone who has never put an 
			obedience title on a dog in the past. Novice B is for someone who has titled a dog 
			in the past, but is not an experienced person in the sport. Novice A and Novice B 
			exercises are the same - Healing on and off lead, Recall with finish, Stand for 
			Exam, Figure 8 pattern, 1 Minute Sit-Stay with Handler in the Ring, 3 minute 
			Down-Stay with Handler in the Ring. The dog must pass each of these tests and 
			have an over-all score of 170 or greater before three different Judges to earn 
			the Title Companion Dog (CD) to go after their 
			Registered Name (e.g. Mom's Little Over-Achiever, CD). 
			<br /><br />
			<img src="images/obedience/obed2.jpg" align="right" width="241" height="193" alt="obedience jump">
			With further training, the dogs move up to the Open A or Open B classes. 
			Open exercises include those demonstrated at the Novice Level except that all are 
			done off-leash. The dog must drop to a down position during the Recall; the 
			Long Sit and Down Stays are performed for three minutes and five minutes each; 
			the dog must retrieve an object on a flat surface and over a high jump, and the 
			dog must jump the broad jump and finish in front of the Handler in a Sit position. 
			The dog must pass each individual exercise with a 50% or higher score and 
			have a final score of 170 points or more, under three separate Judges, 
			to earn the Title Companion Dog Excellent (CDX) after their registered name. 
			<br /><br />
			To move on, the dog must be able to demonstrate and perform the exercises 
			using Hand Signals and be able to scent discriminate articles. This level 
			is called Utility Dog (UD) and is a true demonstration of Handler/Dog 
			communications and training. 
			<br /><br />
			There are non-regulation classes in Obedience that include Pre-Novice, 
			Team Obedience, Brace Obedience and Veterans. Most of these classes are 
			demonstrated at National and Regional Specialty shows and are meant to be fun. 
			No titles are awarded in these non-regulation classes. 
